const saveDB = () => _storage.setItem('ds_db', JSON.stringify(DB));
 
let DB = JSON.parse(JSON.stringify(DEF));
try {
  const saved = JSON.parse(_storage.getItem('ds_db') || 'null');
  if (saved) {
    if ((saved._version || 0) < DEF._version) {
      // [FIX #12] Migration: เก็บ settings + deck/card ที่ user เพิ่มเองไว้
      // แต่ update deck/card ที่มี id ตรงกับ DEF ด้วย content ใหม่
      DB = JSON.parse(JSON.stringify(DEF));
 
      // preserve user settings
      if (saved.settings) DB.settings = { ...DB.settings, ...saved.settings };
 
      // preserve user-added decks (ที่ไม่มีใน DEF)
      const defDeckIds = new Set(DEF.decks.map(d => d.id));
      const userDecks  = (saved.decks || []).filter(d => !defDeckIds.has(d.id));
      const userCards  = {};
      userDecks.forEach(d => {
        userCards[d.id] = saved.cards?.[d.id] || [];
      });
 
      DB.decks  = [...DB.decks, ...userDecks];
      DB.cards  = { ...DB.cards, ...userCards };
 
      saveDB();
    } else {
      DB = saved;
    }
  }
} catch (e) { /* ใช้ DEF เป็น fallback */ }
 
function resetDB() {
  DB = JSON.parse(JSON.stringify(DEF));
  saveDB();
}
